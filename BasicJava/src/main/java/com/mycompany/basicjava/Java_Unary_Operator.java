package com.mycompany.basicjava;
public class Java_Unary_Operator {
    public static void main(String[] args){
        int x = 10;
        
        int result;
        
        result = +x;  // Uninary + 
        System.out.println("Positive : "+result);
        
        
        result = -x;  // Uninary -
        System.out.println("Negative : "+result);
        
        
        
        
        
        
        // Uninary increment ++ and Decrement -- 
        /*
            Increment : 
                Two Type : 
                        1. Prefix : ++a
                        2. Postfix : a++
        
            Decrement : 
                    Two Type : 
                            1. Prefix : --a
                            2. Postfix : a--                
        */
        
    }
}
